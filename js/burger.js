$('document').ready(function () {
    let menu = $(".burgerMenu")
    let background = $('main')
    let also = $('footer')
    let actually = $('#burgeren')
    let decoration1 = $(".decoration1-mobile")
    let decoration2 = $(".decoration2-mobile")
    let decoration3 = $(".decoration3-mobile")
    actually.click(function () {
      if (actually.hasClass("open")) {
        actually.removeClass('open');
        actually.addClass('closed');
        menu.removeClass("burgerMenu-alt").addClass('burgerMenu');
        decoration1.removeClass("decoration1-mobile-alt").addClass('decoration1-mobile');
        decoration2.removeClass("decoration2-mobile-alt").addClass('decoration2-mobile');
        decoration3.removeClass("decoration3-mobile-alt").addClass('decoration3-mobile');
      } else {
        actually.removeClass('closed');
        actually.addClass('open');
        menu.removeClass("burgerMenu").addClass('burgerMenu-alt');
        decoration1.removeClass("decoration1-mobile").addClass('decoration1-mobile-alt');
        decoration2.removeClass("decoration2-mobile").addClass('decoration2-mobile-alt');
        decoration3.removeClass("decoration3-mobile").addClass('decoration3-mobile-alt');
      }
    });
});

